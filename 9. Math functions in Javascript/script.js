//Math.random() wll generate random numbers
console.log(Math.random());

// Math.round() will round of the number 
let a = 3.542;
a = Math.round(a);
console.log(a);

// Math.floor() will round down the number
let b = 3.99;
b = Math.floor(b);
console.log(b);

// Math.ceil() will round up the number (ceil stands for ceiling)
let c = 4.14;
c = Math.ceil(c);
console.log(c);

// Math.pow(number or variable, power) is for raise to power
let d = 2.5;
d = Math.pow(d, 2);
console.log(d);

// Math.sqrt() is for square root operation
let e = 49;
e = Math.sqrt(e);
console.log(e);

// Math.abs() will tell you how far a number is from zero (abs stands for absolute)
let f = -8;
f = Math.abs(f);
console.log(f);

let x = 3, y = 4, z = 5;

// Math.max() will tell you maximum among all
let maximum = Math.max(x, y, z);
console.log("Maximum = ", maximum);

// Math.min() will tell you minimum among all
let minimum = Math.min(x, y, z);
console.log("Minimum = ", minimum);

// Math.PI will tell the actual value of pi
console.log(Math.PI);